        wh3_dlc20_nor_dolgan=true,
        wh3_dlc20_nor_kul=true,
        wh_dlc08_nor_vanaheimlings=true,
        wh_main_nor_aesling=true,
        wh_main_nor_baersonling=true,
        wh_main_nor_bjornling=true,
        wh_main_nor_graeling=true,
        wh_main_nor_sarl=true,
        wh_main_nor_varg=true,
        mixer_nor_fjordlings=true,
        mixer_nor_geimdall_huscarls=true,
        wh_dlc08_nor_naglfarlings=true,
        mixer_nor_beorg=true,
        wh3_dlc20_nor_yusak=true,
}


core:add_listener(
	"rhox_fc_norsca_MonsterHuntMissionSucceeded",
	"MissionSucceeded",
	function(context)
		local faction_key = context:faction():name();
		return rhox_fc_norsca_monster_hunt_factions[faction_key]
	end,
	function(context)
		local faction = context:faction():name();
		local key = context:mission():mission_record_key();
		
		
		local monster_hunt_data_for_faction = monster_hunts[faction]
		if monster_hunt_data_for_faction == nil then
			monster_hunt_data_for_faction = monster_hunts["wh_dlc08_nor_norsca"]
		end	
		
		for i = 1, #monster_hunts_index do
			if key == monster_hunt_data_for_faction[monster_hunts_index[i]]["qb"] then
				if not cm:get_saved_value("norscan_favour_lvl_3_reached_" .. faction) then
                    if faction_key == "wh_main_nor_aesling" then --aesling is locked to Khorne only
                        cm:faction_add_pooled_resource(faction_key, "nor_progress_hound", "events", 12)
                    elseif faction_key == "wh_main_nor_baersonling" then
                        cm:faction_add_pooled_resource(faction_key, "nor_progress_eagle", "events", 12)
                    elseif faction_key == "wh3_dlc20_nor_yusak" then
                        cm:faction_add_pooled_resource(faction_key, "nor_progress_serpent", "events", 12)
                    elseif faction_key == "wh_main_nor_bjornling" then
                        --do nothing
                    else
                        cm:trigger_dilemma(faction, monster_hunt_data_for_faction[monster_hunts_index[i]]["fin"]);
                    end
				end;
				
				deactivate_a_monster_hunt(monster_hunts_index[i], faction);
				
				if key == "wh_dlc08_qb_nor_monster_hunt_6" then
					cm:remove_event_restricted_unit_record_for_faction("wh_dlc08_nor_mon_war_mammoth_ror_1", faction);
				elseif key == "wh_dlc08_qb_nor_monster_hunt_0" then
					cm:remove_event_restricted_unit_record_for_faction("wh_dlc08_nor_mon_frost_wyrm_ror_0", faction);
				end;
			end;
		end;
	end,
	true
);